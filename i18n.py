def get_lang() -> str:
    """取得目前語言設定"""
    return st.session_state.get("lang", "zh")


def set_lang(lang: str):
    """設定語言（'vi' 或 'zh'）"""
    st.session_state["lang"] = lang


def t(key: str, **kwargs) -> str:
    """
    翻譯函數 — 取得當前語言的翻譯文字
    支援格式化參數，例如 t("updated_count", n=5)
    """
    lang = get_lang()
    entry = TRANSLATIONS.get(key, {})
    text = entry.get(lang, entry.get("zh", key))
    if kwargs:
        try:
            text = text.format(**kwargs)
        except (KeyError, ValueError):
            pass
    return text


def render_lang_switcher():
    """在側邊欄渲染語言切換按鈕"""
    current = get_lang()
    
    options = ["🇹🇼 繁體中文", "🇻🇳 Tiếng Việt"]
    current_index = 0 if current == "zh" else 1
    
    col1, col2 = st.columns([2, 3])
    with col1:
        st.markdown("""
        <div style='margin-top: 8px; font-size: 14px; font-weight: bold; color: #666;'>
            🌐 Language
        </div>
        """, unsafe_allow_html=True)
    with col2:
        selected = st.selectbox(
            "Language", 
            options, 
            index=current_index,
            label_visibility="collapsed"
        )
    
    new_lang = "zh" if "繁體中文" in selected else "vi"
    if new_lang != current:
        set_lang(new_lang)
        st.rerun()

